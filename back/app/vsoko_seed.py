def seed_vsoko(db: Session) -> None:
    # Programs
    if db.query(VsokoProgram).count() == 0:
        db.add_all([VsokoProgram(title=t) for t in PROGRAMS_2026])
        db.commit()

    # Questions + options
    if db.query(VsokoQuestion).count() == 0:
        for q in QUESTIONS:
            db.add(VsokoQuestion(id=q["id"], text=q["text"]))
        db.commit()

    if db.query(VsokoOption).count() == 0:
        for q in QUESTIONS:
            for idx, label in enumerate(q["options"]):
                db.add(VsokoOption(question_id=q["id"], label=label, order_index=idx))
        db.commit()